fun main(args: Array<String>){

    // loop through 1 to 200 number and print odd numbers

    for (numbers in 1..200){
        // For Odd Numbers
        if (numbers % 2 == 1){
            println(numbers)
        }
    }

    println("--------------------------------------------------------")

    for (numbers in 1..200){
        // For Even Numbers
        if (numbers % 2 == 0){
            println(numbers)
        }

    }



}

