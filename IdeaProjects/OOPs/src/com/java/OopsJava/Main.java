package com.java.OopsJava;

// Java can have many number of Classes in the Single Program.
// But if we use Public then we should make that public class as the Program Name and as a Main Class
// Classes will Contain Main Method , if the class does not having main method will not compile and
// may have a chance to Produce Bugs in the Code.
// to compile the class , we should keep main method .


public class Main {
    public static void main(String[] args)
    {
        System.out.println(" Main ");
    }
}
class Gain {
    public static void main(String[] args)
    {
        System.out.println(" Gain ");
    }
}
class Pain{
    public static void main(String[] args) {

        System.out.println(" Pain ");
    }
}
class Drain{
    public static void main(String[] args) {

        System.out.println(" Drain ");
    }
}
